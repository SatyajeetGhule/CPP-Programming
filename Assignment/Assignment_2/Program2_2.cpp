//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    Demo dobj;
    int iValue = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    dobj.Display(iValue);
    return 0;
}

