class Demo
{
    public:
    void Display(int iNo, char ch)
    {
        if(iNo == 0)
        return;
        
        printf("%c\t",ch);
    
        ch++;
        Display(iNo -1, ch);
    }
};

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    Demo dobj;
    int iValue = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    dobj.Display(iValue, 'a');
    return 0;
}

