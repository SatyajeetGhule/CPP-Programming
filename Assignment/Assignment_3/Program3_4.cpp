class Demo
{
    public :
    int Fact(int iNo)
    {
        if( iNo == 0 || iNo == 1)
        {
            return 1;
        }
        return iNo * Fact(iNo - 1);
    }
};

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    Demo dobj;
    int iValue = 0;
    int iRet = 0;
    printf("Enter a number :\n");
    scanf("%d",&iValue);
    iRet = dobj.Fact(iValue);
    printf("Factorial value is : %d\n",iRet);
    return 0;
}

