////////////////////////////////////////////////////////////////////////////
//
//  Class Name    :    Demo
//  Function Name :    Fact
//  Description   :    Accept number from user and return its factorial.
//  Input         :    Integer
//  Output        :    Integer
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    11/12/2025
